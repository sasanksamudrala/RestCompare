package com.scalable.diff.restcompare.model;

/**
 * Domain class to maintain Self ref link
 * 
 * @author Sasank Samudrala
 * @version 0.0.1
 */
public class Self {

    private String ref;

    /**
     * Gets the reference link value.
     * 
     * @return the reference link
     */
    public String getRef() {
        return ref;
    }

    /**
     * Sets the reference link value.
     * 
     * @param ref
     *            the reference link
     */
    public void setRef(String ref) {
        this.ref = ref;
    }
    
    
}
